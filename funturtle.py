import turtle
turtle.shape('turtle')
finn = turtle.clone()
finn.shape('square')
finn.goto(200,0)
finn.goto(200,200)
finn.goto(0,200)
finn.goto(0,0)
charlie = turtle.clone()
charlie.shape('triangle')
charlie.penup()
charlie.goto(0,200)
charlie.pendown()
charlie.goto(100,300)
charlie.goto(200,200)
finn.penup()
finn.goto(100,100)
finn.stamp()
finn.goto(0,200)
turtle.stamp()
turtle.penup()
turtle.goto(-100,100)
turtle.clearstamps()
turtle.mainloop()
